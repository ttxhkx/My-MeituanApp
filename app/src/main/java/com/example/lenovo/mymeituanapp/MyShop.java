package com.example.lenovo.mymeituanapp;

public class MyShop {
    private String ShopName;
    private String Owner;
    public MyShop() {
    }

    public String getName() {
        return ShopName;
    }

    public void setName(String name) {
        this.ShopName = name;
    }

    public String getOwner() {
        return Owner;
    }

    public void setOwner(String owner) {
        this.Owner = owner;
    }

}
